# Operador de Adição
a = 5
b = 3
soma = a + b
print("A soma é:", soma)  # Saída: 8

# Operador de Subtração
a = 10
b = 4
subtracao = a - b
print("A subtração é:", subtracao)  # Saída: 6

# Operador de Multiplicação
a = 6
b = 7
multiplicacao = a * b
print("A multiplicação é:", multiplicacao)  # Saída: 42

# Operador de Divisão
numerador = 7
denominador = 3
divisao = numerador / denominador
print("O resultado da divisão é:", divisao)  # Saída: 2.3333333333333335

# Operador de Divisão Inteira
divisao_inteira = numerador // denominador
print("A parte inteira da divisão é:", divisao_inteira)  # Saída: 2

# Operador de Módulo
dividendo = 7
divisor = 3
modulo = dividendo % divisor
print("O resto da divisão é:", modulo)  # Saída: 1

# Operador de Potência
base = 2
expoente = 3
potencia = base ** expoente
print("O resultado da potência é:", potencia)  # Saída: 8

# Precedência de Operadores
resultado = 2 + 3 * 4  # Multiplicação ocorre primeiro
print("Resultado sem parênteses:", resultado)  # Saída: 14

resultado = (2 + 3) * 4  # Parênteses alteram a ordem
print("Resultado com parênteses:", resultado)  # Saída: 20